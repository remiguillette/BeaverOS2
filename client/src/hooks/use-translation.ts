export const useTranslation = create<TranslationState>()(
  persist(
    (set, get) => ({
      currentLang: 'en',
      setLanguage: (lang: 'en' | 'fr') => set({ currentLang: lang }),
      t: (key: string) => {
        const { currentLang } = get();
        return translations[currentLang][key] || key;
      },
    }),
    {
      name: 'beavernet-language',
    }
  )
);